//
//  HW_RESULTs are 16-bit values layed out as follows:
//
//   1 1 1 1 1 1
//   5 4 3 2 1 0 9 8 7 6 5 4 3 2 1 0
//  +-+---------------+-------------+
//  |S|  Facility     |    Code     |
//  +-+---------------+-------------+
//
//  where
//
//      S - Severity - indicates success/fail
//
//          0 - Success
//          1 - Fail
//
//      Facility - is the facility code
//
//      Code - is the facility's status code
//

//
// Severity values
//
#if !defined(SEVERITY_SUCCESS)
#define SEVERITY_SUCCESS    0
#endif

#if !defined(SEVERITY_ERROR)
#define SEVERITY_ERROR      1
#endif

//
// Generic test for success on any status value (non-negative numbers
// indicate success).
//
#define SUCCEEDED(hr) (((UINT16)(hr)) >> 15 == 0)

//
// and the inverse
//
#define FAILED(hr) (((UINT16)(hr)) >> 15 == 1)

//
// Return the code
//
#define HW_RESULT_CODE(hr)      ((hr) & 0x7F)

//
//  Return the facility
//
#define HW_RESULT_FACILITY(hr)  (((hr) & 0x7F80) >> 7)

//
//  Return the severity
//
#define HW_RESULT_SEVERITY(hr)  (((hr) >> 15) & 0x1)

//
// Create an HW_RESULT value from component pieces
//
#define MAKE_HW_RESULT(sev,fac,code) \
    ((HW_RESULT) (((UINT16)(sev)<<15) | ((UINT16)(fac)<<7) | ((UINT16)(code))) )


//
// Easier way to check for fatal error(s), test will end as 'failed' if hr passed in is at SEVERITY_ERROR level
//
#define VERIFY_HR(hr)           do                                                                                                              \
                                {                                                                                                               \
                                    if (FAILED(hr))                                                                                             \
                                    {                                                                                                           \
                                        hw_errmsg("%s: Verify HR failed on line %d of file %s, test failed!\n", __func__, __LINE__, __FILE__);  \
                                        hw_done(HW_TEST_FAIL);                                                                                  \
                                        hw_yield();                                                                                             \
                                    }                                                                                                           \
                                }                                                                                                               \
                                while(FALSE)

